package icu.rolin.easy.model.PO;

public class AssInfoUpdatePO {
    private Integer aid;
    private String name;
    private String intro;
    private String logo;
    private Integer headeruid;

    public Integer getAid() {
        return aid;
    }

    public void setAid(Integer aid) {
        this.aid = aid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getIntro() {
        return intro;
    }

    public void setIntro(String intro) {
        this.intro = intro;
    }

    public String getLogo() {
        return logo;
    }

    public void setLogo(String logo) {
        this.logo = logo;
    }

    public Integer getHeaderuid() {
        return headeruid;
    }

    public void setHeaderuid(Integer headeruid) {
        this.headeruid = headeruid;
    }
}
