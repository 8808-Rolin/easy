package icu.rolin.easy.model.POJO;

public class DataViewPOJO {
    private Integer id;
    private String name;
    private Float num;

    public Integer getUid() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Float getNum() {
        return num;
    }

    public void setNum(Float num) {
        this.num = num;
    }
}
